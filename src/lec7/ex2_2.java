package lec7;

/**
 * Основні методи сервісного класу Math
 */
public class ex2_2 {

    public static void main(String[] args) {

        System.out.println(Math.PI);//3.141592653589793
        System.out.println(Math.pow(2,3));//8.0
        System.out.println(Math.sqrt(4));//2.0
        System.out.println(Math.cbrt(-8));//2.0
        System.out.println(Math.ceil(Math.PI));//4.0
        System.out.println(Math.floor(Math.PI));//3.0
        System.out.println(Math.rint(Math.PI));//3.0
        System.out.println(Math.round(3.5));//3
        System.out.println(Math.random());//0.9397078966163414
        System.out.println(Math.toDegrees(Math.PI));//180.0
        System.out.println(Math.toRadians(180));//3.141592653589793

    }
}
